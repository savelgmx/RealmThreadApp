package com.example.realmthreadapp.model;

import io.realm.RealmObject;

public class Score extends RealmObject {

    private String name;
    private int score;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
